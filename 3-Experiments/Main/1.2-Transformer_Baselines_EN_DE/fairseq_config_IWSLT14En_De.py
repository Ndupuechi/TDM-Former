def get_fairseq_parser(exp_args):


    # ─────────────────────────────────────────────────────────────────────────────────────────────────
    # ✅ ============================= Fairseq parser (model/task configs) ===========================
    # ─────────────────────────────────────────────────────────────────────────────────────────────────

    # ─────────────────────────────────────────────────────────────────────────────────────────────────
    # ✅ use the patched parser

    fs_parser = options.get_training_parser()

    fairseq_args = options.parse_args_and_arch(
        fs_parser,
        input_args=[
            # ✅ Point to EN→De dataset directory
            "data-bin/iwslt14.en-de",


            # ─────────────────────────────────────────────────────────────────────────────────────────────────
            # ✅ === Set correct language directions ===
            # ─────────────────────────────────────────────────────────────────────────────────────────────────
            "--arch", "transformer",        # standard transformer arch
            "--task", "translation",
            "--source-lang", "en",          # source language = English
            "--target-lang", "de",          # target language = German


            # ─────────────────────────────────────────────────────────────────────────────────────────────────
            # ✅ === Model settings ===
            # ─────────────────────────────────────────────────────────────────────────────────────────────────
            "--encoder-layers", "6",
            "--decoder-layers", "6",
            "--encoder-embed-dim", "512",
            "--decoder-embed-dim", "512",
            "--dropout", "0.3",
            "--max-tokens", str(exp_args.max_tokens),  # ✅ keep as string

            # ─────────────────────────────────────────────────────────────────────────────────────────────────
            # ✅ === BLEU evaluation settings ===
            # ─────────────────────────────────────────────────────────────────────────────────────────────────
            "--eval-bleu",
            "--eval-bleu-args", '{"beam": 5, "max_len_a": 1.2, "max_len_b": 10}',
        ],
    )


    cfg = convert_namespace_to_omegaconf(fairseq_args)
    # ─────────────────────────────────────────────────────────────────────────────────────────────────



    return fairseq_args, cfg
   # ─────────────────────────────────────────────────────────────────────────────────────────────────
