# ✅ Import libraries
import torch
import torch.nn as nn


########################################################################################################################
####-------| NOTE 1. DEFINE TANHEXP ACTIVATION CLASS | XXX -----------------------------------------####################
########################################################################################################################



# ===============================================================
# 🔗==================== TANHEXP activation 🔑================🔗
# ===============================================================

class TanhExp(nn.Module):
    def __init__(self):
        super().__init__()

    def forward(self, x):
        # Standard TanhExp activation: x * tanh(exp(x))
        return x * torch.tanh(torch.exp(x))









